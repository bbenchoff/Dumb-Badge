void fillRect(int x1, int y1, int x2, int y2)
{
	if (x1>x2)
	SwapUint16(x1, x2);
	if (y1>y2)
	SwapUint16(y1, y2);
	
	REG_PORT_OUTCLR1 = LCD_CS;
	setXY(x1, y1, x2, y2);
	REG_PORT_OUTSET1 = LCD_DC;
	LCD_Fast_Fill(fore_Color_High, fore_Color_Low,
	(((long)(x2-x1)+1)*((long)(y2-y1)+1)));
	REG_PORT_OUTSET1 = LCD_CS;
}

