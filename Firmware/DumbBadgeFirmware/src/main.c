void LCD_Fast_Fill(int ch, int cl, long pix)
{
	int blocks;
	
		
	REG_PORT_OUTCLR1 = 0x0000ffff;
	REG_PORT_OUTSET1 = (ch << 8) | cl;

	blocks = pix/16;
	for (int i=0; i<blocks; i++)
	{
		for (int j=0; j<16; j++)
		{
			REG_PORT_OUTCLR1 = LCD_WR;
			REG_PORT_OUTSET1 = LCD_WR;
		}
	}
	
	if ((pix % 16) != 0)
	for (int i=0; i<(pix % 16)+1; i++)
	{
		REG_PORT_OUTCLR1 = LCD_WR;
		REG_PORT_OUTSET1 = LCD_WR;
	}
}

